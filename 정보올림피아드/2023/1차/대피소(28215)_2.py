# =============================================================================
# 문제 설명:
# -----------------------------------------------------------------------------
# KOI 마을에는 2차원 평면 상에 N개의 집이 있습니다.
# 각 집의 위치는 (X_i, Y_i)로 주어지며, 두 집 사이의 거리는 맨해튼 거리로
# 계산됩니다. 즉, 두 집 (X_i, Y_i)와 (X_j, Y_j) 사이의 거리는
# |X_i - X_j| + |Y_i - Y_j| 로 구해집니다.
#
# 재난 상황에서 주민들이 안전하게 대피할 수 있도록, 마을의 K개의 집에
# 대피소를 설치하려고 합니다. 모든 집은 자신과 가장 가까운 대피소로 이동할 때,
# 이동해야 하는 거리(맨해튼 거리)가 발생합니다.
#
# 목표는 "모든 집들이 대피소로 이동할 때, 가장 멀리 있는 집까지의 거리가
# 최소가 되는" 대피소 설치 방법을 찾는 것입니다.
#
# 구체적으로, 아래의 과정을 거칩니다:
# 1. N개의 집 중 K개의 집을 대피소로 선택하는 모든 경우의 수를 고려합니다.
# 2. 각 경우에 대해, 모든 집에 대해 해당 집에서 선택된 대피소들 중
#    가장 가까운 대피소와의 거리를 구합니다.
# 3. 각 경우마다, 모든 집에 대해 구한 "가장 가까운 대피소까지의 거리" 중에서
#    최댓값(즉, 가장 멀리 있는 집의 대피소까지의 거리)을 찾습니다.
# 4. 모든 경우 중에서 이 최댓값이 최소가 되는 경우의 값을 출력합니다.
#
# =============================================================================
#
# 입력:
# - 첫 줄에 정수 N (집의 개수)과 K (설치할 대피소의 개수)가 공백으로 구분되어 주어집니다.
# - 이어서 N개의 줄에 걸쳐 각 집의 좌표 X_i와 Y_i가 주어집니다.
#
# 출력:
# - 모든 가능한 대피소 설치 방법 중, 각 집이 가장 가까운 대피소로 이동할 때
#   발생하는 최대 이동 거리가 최소가 되는 경우의 거리를 출력합니다.
#
# 제한:
# - 1 ≤ K ≤ 3, K ≤ N ≤ 50
# - 0 ≤ X_i, Y_i ≤ 100,000
#
# 예제:
# 예제 입력 1:
# 5 2
# 1 5
# 3 0
# 3 3
# 6 12
# 8 9
#
# 예제 출력 1:
# 5
#
# =============================================================================
#
# 코드 설명:
# -----------------------------------------------------------------------------
# 이 코드는 아래와 같은 알고리즘을 사용합니다.
#
# 1. itertools 모듈의 combinations 함수를 사용하여, 전체 집들 중에서
#    K개의 집을 선택하는 모든 조합을 만듭니다.
#
# 2. 각 조합(대피소 설치 위치)에 대해, 모든 집을 순회하면서 해당 집에서
#    선택된 대피소들 중 가장 가까운 대피소와의 맨해튼 거리를 계산합니다.
#
# 3. 각 집마다 계산한 "가장 가까운 대피소까지의 거리" 중 최대값을 구합니다.
#    이 최대값은 해당 조합에서 "가장 멀리 떨어진 집까지의 대피소 거리"를 의미합니다.
#
# 4. 모든 조합에 대해 위의 최대값을 구한 후, 이 값들이 가지는 최소값을 찾습니다.
#
# 5. 최종적으로, 모든 집이 대피소로 이동할 때 발생하는 최대 이동 거리가 최소가 되는 경우의
#    거리를 출력합니다.
#
# 초등학생도 이해할 수 있도록 쉽게 말하면:
# "마을에 있는 모든 집들을 보고, 몇 개의 집에 대피소를 만들었을 때,
# 모든 집들이 제일 가까운 대피소로 갈 때 걸리는 거리 중 가장 긴 거리가
# 최소가 되는 방법을 찾는 문제입니다."
#
# =============================================================================
#
# 코드 구현:
# -----------------------------------------------------------------------------
from itertools import combinations  # 여러 개의 원소 중에서 k개를 뽑는 모든 조합을 만들어 줍니다.

# 입력 받기: 첫 줄에서 집의 개수 n과 대피소의 개수 k를 입력 받습니다.
n, k = map(int, input().split())

# 각 집의 좌표를 저장할 리스트를 초기화합니다.
# x와 y 리스트는 각각 집들의 x좌표와 y좌표를 저장합니다.
x = [0] * n
y = [0] * n

# 임시 변수 dist를 매우 큰 값으로 초기화합니다.
# (이 변수는 나중에 거리 계산 시 사용되지만, 여기서는 단순 초기화 목적입니다.)
dist = 100000000

# 다음 n개의 줄에서 각 집의 좌표를 입력 받아 x와 y 리스트에 저장합니다.
for i in range(n):
    x[i], y[i] = map(int, input().split())

# 최종 답을 저장할 변수 ans를 매우 큰 값으로 초기화합니다.
# 이 변수는 가능한 모든 조합 중 최소의 최대 거리를 저장할 것입니다.
ans = 100000000

# 모든 집들 중에서 k개의 집을 대피소로 선택하는 모든 경우를 고려합니다.
for shelter_indices in combinations(range(n), k):
    # 변수 a는 현재 대피소 조합에 대해, 모든 집들 중 가장 멀리 있는 집의 대피소까지의 거리를 저장합니다.
    a = 0
    # 모든 집 j에 대해 반복합니다.
    for j in range(n):
        # 현재 집 j에서 선택된 대피소들 중 가장 가까운 대피소와의 거리를 구하기 위해
        # min_dist를 아주 큰 값으로 초기화합니다.
        min_dist = 1000000000
        # 현재 조합에서 선택된 각 대피소 shelter에 대해 반복합니다.
        for shelter in shelter_indices:
            # 맨해튼 거리 계산: 두 집 사이의 거리는 |x[j] - x[shelter]| + |y[j] - y[shelter]| 입니다.
            current_distance = abs(x[j] - x[shelter]) + abs(y[j] - y[shelter])
            # 현재 계산된 거리가 지금까지의 최소 거리보다 작으면 업데이트합니다.
            if current_distance < min_dist:
                min_dist = current_distance
        # 모든 대피소에 대해 계산한 후, 집 j의 "가장 가까운 대피소까지의 거리" min_dist를
        # 현재 조합의 최대 거리 a와 비교하여 더 큰 값으로 업데이트합니다.
        if min_dist > a:
            a = min_dist
    # 모든 집에 대한 계산이 끝난 후, 이 조합에서의 최대 거리 a와 지금까지의 최적 값 ans를 비교하여
    # 더 작은 값을 ans에 저장합니다.
    if a < ans:
        ans = a

# 최종적으로, 모든 조합 중에서 "가장 멀리 떨어진 집까지의 거리"의 최소값인 ans를 출력합니다.
print(ans)
