"""
문제: 스케이트 연습

문제 설명:
- 코스: 시작, N개의 중간, 도착.
- 시작/도착: 속도 0. 중간: 속도 0 불가.
- 각 중간: 속도 제한 V_i.
- 성과: 중간 속도 합.
- 목표: 속도 합 최대화.

속도 규칙:
- 올릴 때: 원하는 만큼 올림.
- 줄일 때: 이전 속도에서 딱 1만 줄임.

풀이:
1. 마지막 속도는 무조건 1.
이유
- 0의 속력으로 도착지점에 도착하려면 마지막 속도는 1만 가능
- 역으로 계산하면 마지막 지점의 1을 기준으로 앞쪽에서
- 가능한 최대 속도를 쉽게 결정 가능

2. 역으로 계산: 마지막부터 첫 번째.
   - 각 지점: 다음 속도 기준, 최대 (before_speed + 1).
   - 선택: min(V_i, before_speed + 1).
3. 속도 합 산출.

예시 (N = 3, V = [2, 3, 1]):
- 마지막: 1.
- 두 번째: 1+1=2, 제한 3 → 선택: min(3,2)=2.
- 첫 번째: 2+1=3, 제한 2 → 선택: min(2,3)=2.
- 합: 2+2+1=5.
"""

def solve1():
    N = int(input())
    V = list(map(int, input().split()))

    result = 1         # 마지막 속도는 무조건1이기 때문에, 마지막 속도를 미리 추가
    before_speed = 1   # 이전 지점 속도(인덱스 기준으로 우측)

    # 역순 계산: 마지막 전부터 첫 번째까지
    for i in range(-1, -len(V), -1):
        # V[i-1]: 현재 지점 제한
        # before_speed: 이전 지점 속도
        if V[i-1] > before_speed:
            before_speed += 1  # 1 증가 가능
        else:
            before_speed = V[i-1]  # 제한에 맞춤
        result += before_speed  # 결과에 추가

    # 속도 합 출력
    print(result)

def solve2():
    n = int(input())
    t = list(map(int, input().split()))

    # s: 현재 계산 중인 지점에서 가능한 최대 속도
    # 마지막 중간 지점은 도착 대비 1이므로 초기값을 1로 설정
    s = 1

    # re: 속도 합을 저장할 변수, 마지막 지점의 속도 1부터 시작
    re = 1

    # 리스트 t를 역순으로 바꿔서, 마지막 지점부터 계산하기 쉽게 함
    t.reverse()

    # 역순으로 두 번째 지점부터 첫 번째 지점까지 순회 (인덱스 1부터 n-1까지)
    for i in range(1, n):
        # 만약 현재 최대 속도 s가 현재 지점 제한 t[i]보다 작으면,
        # 이전 지점 속도 s보다 1 크게 할 수 있음
        if s < t[i]:
            s += 1  # 속도를 1 증가시킴
        else:
            # 현재 지점의 속도 제한을 초과할 수 없으므로, t[i]로 맞춤
            s = t[i]
        # 현재 지점의 결정된 속도를 결과에 더함
        re += s

    # 계산한 속도 합을 출력
    print(re)

def solve3():
    n = int(input())
    v = list(map(int, input().split()))

    # tmp: 각 지점에서 가능한 최대 속도를 저장할 리스트
    # 모든 지점을 1로 초기화 (마지막 지점은 도착 대비 1이어야 하므로)
    tmp = [1] * n

    # 문제 풀이를 위해 v 리스트를 역순으로 뒤집음
    v.reverse()

    # 역순으로, 두 번째 지점부터 첫 번째 지점까지 계산
    for i in range(1, n):
        # 만약 현재 지점의 속도 제한이 1이면, 어쩔 수 없이 속도는 1밖에 안 됨
        if v[i] == 1:
            tmp[i] = 1
        # 현재 지점의 제한이 이전 지점에서 가능한 속도보다 크면,
        # 이전 지점 속도에 1을 더한 값까지 속도를 올릴 수 있음
        elif v[i] > tmp[i - 1]:
            tmp[i] = tmp[i - 1] + 1
        # 현재 지점의 제한과 이전 지점의 가능한 속도가 같으면,
        # 속도를 그대로 유지함
        elif v[i] == tmp[i - 1]:
            tmp[i] = tmp[i - 1]
        # 그 외의 경우, 현재 지점의 제한이 이전 지점의 속도보다 낮으므로
        # 속도를 현재 제한에 맞춤
        else:
            tmp[i] = v[i]

    # 모든 지점의 속도를 더한 값(최대 연습 성과)을 출력
    print(sum(tmp))


if __name__ == '__main__':
    solve1()
    solve2()
    solve3()
