"""
문제: 불안정한 수열 만들기

문제 설명:
- N개의 자연수 A1, A2, ..., AN가 주어진다.
- 수열에서 순서를 유지하며 일부 원소를 선택해 새로운 수열 B를 만든다.
- B가 불안정한 수열이 되려면 인접한 두 원소의 합이 모두 홀수여야 한다.
  (즉, 홀수와 짝수가 번갈아 나타나야 함)
- k = 1인 경우에도 B는 불안정한 수열로 간주한다.
- 목표는 불안정한 수열을 만들 때 선택할 수 있는 원소의 개수를 최대화하는 것이다.

아래는 이 문제를 풀 수 있는 여러 가지 접근 방법에 대한 설명이다.

1. Greedy (그리디) 접근:
   - **핵심 아이디어:** 첫 번째 원소는 무조건 선택하고, 이후 원소를 순차적으로 탐색하면서
     이전에 선택한 원소와 현재 원소의 홀짝이 다르면 선택한다.
   - **이유:** 두 수의 합이 홀수가 되려면 한 수는 홀수, 다른 수는 짝수여야 하므로,
     번갈아 나오는 패턴(홀, 짝 또는 짝, 홀)을 유지하는 것이 최적이다.
   - **시간 복잡도:** O(n)

2. 압축(Compressed Group) 접근:
   - **핵심 아이디어:** 수열을 연속된 동일한 홀짝 그룹으로 압축한다.
     각 그룹에서는 하나의 원소만 선택하면 번갈아 나오는 패턴에 영향을 주지 않는다.
   - **방법:** 첫 원소를 압축 리스트에 넣고, 이후 원소가 마지막 압축 원소와 다른 홀짝이면 압축 리스트에 추가한다.
   - **결과:** 압축된 리스트의 길이가 최대 선택할 수 있는 원소의 개수가 된다.
   - **시간 복잡도:** O(n)

3.  인접한 요소의 합으로(i + i+1) 접근:
    # 핵심 아이디어:
    # - 인접한 두 숫자의 합이 짝수이면, 두 숫자는 모두 홀수이거나 모두 짝수이다.
    # - 불안정한 수열(인접한 합이 홀수)이 되려면 홀수와 짝수가 번갈아 나와야 한다.
    #   즉, 인접한 숫자가 같은 홀짝이면 번갈아 나오는 조건을 위반한다.
    # - 따라서, 같은 홀짝인 인접 쌍의 개수를 세고, 전체 숫자 개수에서 빼면
    #   불안정한 수열을 만들 때 선택할 수 있는 최대 원소의 수가 된다.

    # 이유:
    # - 인접한 두 숫자가 같은 홀짝이면 그 두 숫자를 모두 선택할 수 없다.
    # - 그래서 그런 쌍의 개수만큼 선택할 수 있는 원소가 줄어든다고 볼 수 있다.

    # 시간 복잡도:
    # - O(n): 수열을 한 번 순회하므로 입력 크기에 비례한 시간이 소요된다.

"""


#---------------------------------------------
#1. Greedy 접근 코드 (효율적, O(n)):
#---------------------------------------------
"""
def greedy():
    # 수열의 길이 n을 입력받는다.
    n = int(input())
    # n개의 자연수를 공백 기준으로 입력받아 리스트로 변환한다.
    arr = list(map(int, input().split()))

    # 첫 번째 원소는 무조건 선택하므로 선택된 원소의 개수를 1로 초기화한다.
    count = 1
    # 첫 번째 원소의 홀짝 정보를 저장한다 (홀수면 1, 짝수면 0).
    last_parity = arr[0] % 2

    # 두 번째 원소부터 순서대로 확인한다.
    for number in arr[1:]:
        # 현재 원소의 홀짝을 계산한다.
        if number % 2 != last_parity:
            # 이전에 선택한 원소와 현재 원소의 홀짝이 다르면 선택한다.
            count += 1
            # 현재 원소의 홀짝 정보를 갱신한다.
            last_parity = number % 2
    # 최대 선택 가능한 원소의 수를 출력한다.
    print(count)
"""


#---------------------------------------------
#2. 압축(Compressed Group) 접근 코드 (효율적, O(n)):
#---------------------------------------------
"""
def compressed():
    # 수열의 길이 n을 입력받는다.
    n = int(input())
    # n개의 자연수를 입력받아 리스트로 만든다.
    arr = list(map(int, input().split()))

    # 압축된 리스트를 만들고, 첫 번째 원소는 무조건 추가한다.
    compressed = [arr[0]]

    # 두 번째 원소부터 차례대로 확인하며, 이전 압축 원소와 홀짝이 다르면 추가한다.
    for num in arr[1:]:
        if num % 2 != compressed[-1] % 2:
            compressed.append(num)
    # 압축된 리스트의 길이가 최대 선택 가능한 원소의 수이다.
    print(len(compressed))
"""

#---------------------------------------------
#3.  인접한 요소의 합으로(i + i+1) 접근:
#---------------------------------------------

def even_counter():
    # 수열의 길이 n을 입력받는다.
    n = int(input())
    # n개의 자연수를 입력받아 리스트로 만든다.
    arr = list(map(int, input().split()))

    even_count = 0
    for i in range(len(arr)-1):
        if (arr[i] + arr[i+1]) %2==0:
            even_count +=1
    # 수열의 길이에서 요소의 합이 짝수인 경우의 수를 제거한
    # 리스트의 길이가 최대 선택 가능한 원소의 수이다.
    print(n-even_count)


