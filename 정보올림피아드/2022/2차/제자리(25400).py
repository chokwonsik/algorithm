"""
문제: 제자리

문제 요약:
- N장의 카드가 왼쪽부터 나열되어 있고, 각 카드에는 정수 A_i가 적혀있다.
- 원하는 상태(제자리 상태)는 남은 카드들이 왼쪽부터 순서대로 1, 2, 3, ...가 되는 것이다.
- 몇 장의 카드를 제거하여 남은 카드들이 [1, 2, 3, ..., k]가 되게 하는데,
  최소 제거 카드 수 = 전체 카드 수 N - (k) 이다.

풀이 방법:
1. Greedy 방법 (O(N)):
   - 간단하게 A를 순회하며, "찾을 숫자(next_num)"가 나오면 next_num을 1 증가시킨다.
   - 최종적으로 next_num - 1이 만들 수 있는 최대 연속 부분수열의 길이이다.

"""

#---------------------------------------------
#1. Greedy 접근 코드 (효율적, O(N)):
#---------------------------------------------

def greedy():
    # Greedy 방법:
    # 카드 배열 A를 왼쪽부터 순회하며, 찾고자 하는 숫자(1부터 시작)가 나오면 다음 숫자로 업데이트.
    # 시간복잡도: O(N)

    N = int(input().strip())
    A = list(map(int, input().split()))

    next_num = 1  # 처음에는 1부터 찾기 시작
    for a in A:
        if a == next_num:
            next_num += 1  # 원하는 숫자를 찾으면 다음 숫자로 업데이트

    # 부분수열의 길이는 next_num - 1이고, 제거해야 하는 카드 수는 전체 카드 수 N에서 이를 뺀 값
    print(N - (next_num - 1))

if __name__ == '__main__':
    # 1. Greedy (O(N)):
    greedy()