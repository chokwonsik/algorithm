# 야구 시즌
# https://www.acmicpc.net/problem/21760
def solve1():
    "정답 코드 1: 직접 수식 이용 풀이 (시간복잡도 O(1))"
    import sys
    input = sys.stdin.readline

    # ========================================================
    # 문제 풀이 설명
    #
    # 1. 입력값: N, M, k, D
    #    - N: 지역리그 개수, M: 각 리그 팀 수, k: A = k * B (상수), D: 경기 수 제한
    #
    # 2. 경기 수 계산
    #    - 같은 지역리그 경기: 한 리그 내 두 팀이 경기하는 경우의 수는 M*(M-1)/2,
    #      각 경기마다 A번 진행하므로, 한 리그의 경기 수는 A * M*(M-1)/2.
    #      전체 리그는 N배 → N * A * M*(M-1)/2.
    #
    #    - 다른 지역리그 경기:
    #      전체 팀 쌍은 (N*M)*(N*M-1)/2에서, 같은 지역 쌍 (N * M*(M-1)/2)를 빼면,
    #      다른 지역 팀 쌍 수가 나온다.
    #      각 쌍마다 B번 경기 → B * {[(N*M)*(N*M-1)/2] - [N * M*(M-1)/2]}.
    #
    #    - A = k * B 이므로, 전체 경기 수를 B에 대한 식으로 정리하면:
    #      Total = B/2 * [N*k*M*(M-1) + N*(N-1)*M^2].
    #
    # 3. 정의: Q = N*k*M*(M-1) + N*(N-1)*M^2, 그러면
    #    Total = B * (Q/2)
    #
    # 4. 목표: 총 경기 수가 D 이하이면서 D에 가장 가깝도록 하기
    #    → B * (Q/2) <= D 인 최대의 B를 구한다.
    #    → B = floor(D / (Q/2)). 단, B는 최소 1이어야 한다.
    #
    # 5. 만약 B < 1이면 조건을 만족하는 경우가 없으므로 -1을 출력.
    # ========================================================

    # 테스트 케이스의 개수 T 입력
    T = int(input())

    for _ in range(T):
        # 입력: N, M, k, D
        N, M, k, D = map(int, input().split())

        # Q = N*k*M*(M-1) + N*(N-1)*M^2
        Q = N * k * M * (M - 1) + N * (N - 1) * M * M

        # Q는 항상 짝수이므로 Q//2는 정수이며,
        # Total 경기 수 = B * (Q//2)
        Q2 = Q // 2

        # 최대 B는 D를 Q2로 나눈 몫 (조건: B * Q2 <= D)
        B = D // Q2

        # 각 팀은 최소 1경기 이상 해야하므로, B가 1 미만이면 조건 불만족
        if B < 1:
            sys.stdout.write("-1\n")
        else:
            total_matches = B * Q2  # 전체 경기 수 계산
            sys.stdout.write(str(total_matches) + "\n")


def solve2():
    "정답 코드 2: 이진 탐색 이용 풀이 (시간복잡도 O(log D))"
    import sys
    input = sys.stdin.readline

    # ========================================================
    # 문제 풀이 설명
    #
    # 1. 이전 풀이와 같이 Total 경기 수는 B * (Q/2)로 계산됨.
    #    Q = N*k*M*(M-1) + N*(N-1)*M^2.
    #
    # 2. 이번 방법은 직접 B = floor(D / (Q/2))를 계산하는 대신,
    #    이진 탐색을 이용해 B값을 찾는다.
    #
    # 3. B의 가능한 범위는 [1, D] (D가 최댓값이 될 수 있음)
    #
    # 4. 이진 탐색을 수행하면서, mid값에 대해
    #    total = mid * (Q//2)를 계산하여 D 이하인지 확인.
    #    만약 total <= D이면 조건 만족하므로 후보값 업데이트 후, 더 큰 mid 탐색.
    #    아니면 hi를 줄여서 탐색.
    #
    # 5. 이진 탐색이 끝난 후, 조건을 만족하는 최대 total 값을 출력.
    # ========================================================

    T = int(input())

    for _ in range(T):
        N, M, k, D = map(int, input().split())

        # Q = N*k*M*(M-1) + N*(N-1)*M^2
        Q = N * k * M * (M - 1) + N * (N - 1) * M * M
        Q2 = Q // 2  # Q는 항상 짝수

        # 이진 탐색을 위한 초기 범위 설정: B는 최소 1, 최대 D
        lo, hi = 1, D
        ans = -1  # 조건을 만족하는 경우가 없으면 -1
        while lo <= hi:
            mid = (lo + hi) // 2  # 현재 후보 B값
            total = mid * Q2  # 해당 B값일 때의 전체 경기 수
            if total <= D:
                ans = total  # 조건을 만족하면 후보값 업데이트
                lo = mid + 1  # 더 큰 B값이 가능한지 탐색
            else:
                hi = mid - 1  # total이 D보다 크면 B값 줄이기
        sys.stdout.write(str(ans) + "\n")


