# 보물찾기(32068)_2.py
# 
# 1. 보물 위치 및 시작 위치:
#    - 보물은 두 곳에 숨겨져 있으며, 위치는 L과 R로 주어진다. (L < R)
#    - 다빈이는 시작 위치 S에서 출발하며, 항상 L < S < R를 만족한다.
#
# 2. 이동 규칙:
#    - 1단계: S에서 시작 위치를 조사한다.
#    - 2단계: 오른쪽으로 1칸 이동 → S + 1을 조사한다.
#    - 3단계: 왼쪽으로 2칸 이동 → S + 1 - 2 = S - 1을 조사한다.
#    - 4단계: 오른쪽으로 3칸 이동 → S - 1 + 3 = S + 2를 조사한다.
#    - 5단계: 왼쪽으로 4칸 이동 → S + 2 - 4 = S - 2를 조사한다.
#    - 이후에도 같은 패턴으로 진행된다.
#
# 3. 보물 찾는 단계 계산:
#    - 오른쪽 보물(R)에 도달하기 위해서는 S에서 오른쪽으로 (R - S)칸 이동해야 한다.
#      오른쪽 이동은 2, 4, 6, ... 번째 단계에서 일어나므로,
#      오른쪽 보물은 2*(R-S)번째 단계에서 발견된다.
#
#    - 왼쪽 보물(L)에 도달하기 위해서는 S에서 왼쪽으로 (S - L)칸 이동해야 한다.
#      왼쪽 이동은 3, 5, 7, ... 번째 단계에서 일어나므로,
#      왼쪽 보물은 2*(S-L) + 1번째 단계에서 발견된다.
#
# 4. 최종 결과:
#    - 두 보물 중 더 먼저 도착하는 쪽이 보물을 찾는 단계가 된다.
#    - 따라서 정답은 min(2*(R-S), 2*(S-L)+1) 이다.
#

T = int(input())  # 테스트 케이스의 개수를 입력받는다.
for _ in range(T):
    # 보물이 숨겨진 위치 L, R와 시작 위치 S를 공백으로 구분하여 입력받는다.
    L, R, S = map(int, input().split())

    # 오른쪽 보물에 도달하는 단계 계산:
    # S에서 R까지 이동해야 하는 칸 수는 (R - S)이다.
    # 오른쪽 이동은 2, 4, 6번째 단계에서 진행되므로, 보물을 찾는 단계는 2*(R-S)이다.
    right_steps = 2 * (R - S)

    # 왼쪽 보물에 도달하는 단계 계산:
    # S에서 L까지 이동해야 하는 칸 수는 (S - L)이다.
    # 왼쪽 이동은 3, 5, 7번째 단계에서 진행되므로, 보물을 찾는 단계는 2*(S-L) + 1이다.
    left_steps = 2 * (S - L) + 1

    # 두 보물 중 더 빠르게 도달하는 단계를 출력한다.
    print(min(right_steps, left_steps))