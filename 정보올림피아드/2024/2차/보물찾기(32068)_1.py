# 보물찾기(32068)_1.py
#
# 1. 문제 개요:
#    - 무한한 수직선 상에 두 개의 보물이 숨겨져 있습니다.
#      보물은 각각 위치 L과 R에 숨겨져 있으며, L < R임이 보장됩니다.
#    - 다빈이는 시작 위치 S에서 출발하는데, 항상 L < S < R를 만족합니다.
#
# 2. 이동 규칙:
#    - 1단계: 시작 위치 S에서 보물이 있는지 조사합니다.
#    - 2단계: 오른쪽으로 1칸 이동하여 S + 1을 조사합니다.
#    - 3단계: 왼쪽으로 2칸 이동하여 (S + 1) - 2 = S - 1을 조사합니다.
#    - 4단계: 오른쪽으로 3칸 이동하여 (S - 1) + 3 = S + 2를 조사합니다.
#    - 5단계: 왼쪽으로 4칸 이동하여 (S + 2) - 4 = S - 2를 조사합니다.
#    - 이와 같이, 각 단계에서 이동하는 칸의 수는 "단계 번호 - 1"이고,
#      짝수 단계에서는 오른쪽(큰 수 방향)으로, 홀수 단계에서는 왼쪽(작은 수 방향)으로 이동합니다.
#
# 3. 문제 해결:
#    - 각 단계마다 현재 위치를 갱신하면서 보물이 숨겨진 위치(L 또는 R)에 도달하는지를 검사합니다.
#    - 보물이 발견되면, 해당 단계 번호를 출력하고 시뮬레이션을 종료합니다.


T = int(input())  # 테스트 케이스의 개수를 입력받습니다.
for _ in range(T):
    # 보물이 숨겨진 위치 L, R와 시작 위치 S를 공백으로 구분하여 입력받습니다.
    L, R, S = map(int, input().split())

    # 현재 위치(current_position)는 시작 위치 S로 초기화됩니다.
    current_position = S
    # step 변수는 현재 단계 번호를 나타냅니다.
    # 1단계는 시작 위치 S를 조사하는 단계입니다.
    step = 1

    # 보물을 찾을 때까지 무한 루프를 돌면서 이동을 시뮬레이션합니다.
    while True:
        step += 1  # 다음 단계로 넘어가기 위해 단계 번호를 1 증가시킵니다.

        # 이번 단계에서 이동할 칸의 수는 '현재 단계 번호 - 1'입니다.
        # 예를 들어, 2단계에서는 1칸, 3단계에서는 2칸, 4단계에서는 3칸 이동합니다.
        move_distance = step - 1

        # 이동 방향 결정:
        # - 짝수 단계면 오른쪽(큰 수 방향)으로 이동합니다.
        # - 홀수 단계면 왼쪽(작은 수 방향)으로 이동합니다.
        if step % 2 == 0:
            # 짝수 단계: 오른쪽으로 move_distance 칸 이동
            current_position += move_distance
        else:
            # 홀수 단계: 왼쪽으로 move_distance 칸 이동
            current_position -= move_distance

        # 이동 후 현재 위치가 보물이 숨겨진 위치(L 또는 R)와 일치하는지 확인합니다.
        if current_position == L or current_position == R:
            # 보물이 발견되면, 현재 단계 번호를 출력하고 while 루프를 종료합니다.
            print(step)
            break

