# 등교(31962)_1.py
# -------------------------------------------------------------
# 문제: 등교 성공서브태스크
# 목표: 학교에 X분 이내 도착할 수 있는 버스 중,
#       가장 늦게 출발하는 버스의 출발 시간(S)을 구하는 문제입니다.
#
# 전체 설명:
# 1. 입력을 받아 버스의 개수 N과 학교 도착 제한 시간 X를 저장합니다.
# 2. 각 버스마다 정류장에서 출발하기까지 남은 시간 S와
#    정류장에서 학교까지 걸리는 시간 T를 입력받습니다.
# 3. 만약 버스가 출발한 후 학교에 도착하는 총 시간이 (S + T) X분 이하이면,
#    해당 버스의 출발 시간 S를 리스트에 저장합니다.
# 4. 리스트에 저장된 버스들 중에서 가장 큰 S (즉, 가장 늦게 출발하는 버스)를 출력합니다.
# 5. 조건을 만족하는 버스가 하나도 없다면 -1을 출력합니다.
# -------------------------------------------------------------

# 1. 먼저, 입력을 통해 버스의 개수 N과 학교 도착 제한 시간 X를 읽어옵니다.
# input() 함수는 문자열을 입력받으므로, map()을 사용해 정수형으로 변환합니다.
N, X = map(int, input().split())

# 2. 조건을 만족하는 버스의 출발 시간(S)를 저장할 빈 리스트를 만듭니다.
time = []

# 3. 총 N개의 버스 정보를 한 줄씩 입력받으며 확인합니다.
for i in range(N):
    # 각 줄에서 버스의 출발까지 남은 시간 S와 학교까지 걸리는 시간 T를 입력받습니다.
    S, T = map(int, input().split())

    # 4. 만약 이 버스가 X분 이내에 학교에 도착할 수 있다면,
    #    즉, 출발 시간 S와 이동 시간 T를 합한 값이 X 이하이면 조건을 만족합니다.
    if S + T <= X:
        # 조건을 만족하면 이 버스의 출발 시간 S를 리스트에 추가합니다.
        time.append(S)

# 5. 조건을 만족하는 버스가 하나라도 있다면, 리스트 time에는 하나 이상의 출발 시간 값이 저장되어 있습니다.
if time:
    # 리스트 내 값들 중 최대값(max)을 찾으면, 가장 늦게 출발하는 버스의 출발 시간입니다.
    print(max(time))
# 6. 만약 리스트가 비어있다면, 즉 조건을 만족하는 버스가 하나도 없다면 -1을 출력합니다.
else:
    print(-1)
