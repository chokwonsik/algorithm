# 등교(31962)_2.py
# -------------------------------------------------------------
# 문제: 등교 성공서브태스크
# 목표: 학교에 X분 이내 도착할 수 있는 버스 중,
#       가장 늦게 출발하는 버스의 출발 시간(S)을 구하는 문제입니다.
#
# 전체 설명:
# 1. 입력을 받아 버스의 개수 N과 학교 도착 제한 시간 X를 저장합니다.
# 2. 변수 'answer'를 -1로 초기화합니다.
#    - 초기값이 -1인 이유는, 조건을 만족하는 버스가 없을 경우 그대로 -1을 출력하기 위해서입니다.
# 3. 각 버스마다 정류장에서 출발까지 남은 시간 S와 이동 시간 T를 입력받습니다.
# 4. 만약 버스가 X분 이내에 학교에 도착할 수 있다면 (즉, S + T <= X),
#    지금까지 저장된 'answer'와 현재 버스의 출발 시간 S 중 큰 값을 'answer'로 갱신합니다.
# 5. 모든 버스 정보를 확인한 후, 조건을 만족하는 버스의 출발 시간 중 최대값인 answer를 출력합니다.
# -------------------------------------------------------------

# 1. 사용자로부터 버스의 개수 N과 학교 도착 제한 시간 X를 입력받아 정수로 변환합니다.
N, X = map(int, input().split())

# 2. 조건을 만족하는 버스가 없을 때를 대비해 answer를 -1로 초기화합니다.
answer = -1

# 3. 총 N개의 버스 정보를 한 줄씩 입력받습니다.
for _ in range(N):
    # 각 줄에서 버스의 출발까지 남은 시간 S와 학교까지 걸리는 시간 T를 읽어옵니다.
    S, T = map(int, input().split())

    # 4. 버스가 학교에 도착하는 총 시간이 X분 이하인지 확인합니다.
    if S + T <= X:
        # 조건을 만족하면, 현재까지의 최대 출발 시간(answer)과 현재 버스의 출발 시간 S 중 더 큰 값을 answer에 저장합니다.
        answer = max(answer, S)

# 5. for문이 종료된 후, 조건을 만족하는 버스가 있다면 answer에 그 최대값이 저장되어 있습니다.
#    만약 조건을 만족하는 버스가 하나도 없었다면 answer는 여전히 -1입니다.
print(answer)
