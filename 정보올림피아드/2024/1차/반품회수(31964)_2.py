# 반품회수(31964)_2.py
"""
솔루션 2: 최대값 접근 방식

문제 설명:
-------------
- N개의 집이 직선 상에 있고, 각 집 i는 위치 X[i]에 있으며, 집 i에서는 시각 T[i]에 물건이 회수됩니다.
- 트럭은 0번 위치(회사)에서 출발하여 모든 집을 방문한 후 다시 0으로 돌아와야 합니다.
- 트럭은 1의 속도로 이동하며, 물건 회수는 도착 시각이 T[i] 이상이면 즉시 가능합니다.

풀이 아이디어:
-------------
- 트럭이 집을 모두 방문하고 회사로 돌아오는 기본적인 시간은 오른쪽 끝 집까지의 왕복 시간, 즉 2 * X[N-1] 입니다.
- 하지만, 각 집 i에서는 물건이 준비되는 T[i]에 맞춰 도착해야 하므로, 집 i에서는 최소 T[i] + X[i] 시간이 필요합니다.
  - 여기서 X[i]는 0에서 집 i까지 이동하는데 필요한 시간이며, T[i]가 더 크면 기다려야 하기 때문에 T[i] + X[i]로 표현됩니다.
- 따라서 모든 집에 대해 T[i] + X[i]를 계산하고, 이들 중 최대값과 기본 왕복 시간 2 * X[N-1] 중 큰 값이 최종 답이 됩니다.

알고리즘 상세:
-------------
1. 기본 왕복 시간: base_time = 2 * X[N-1].
2. 각 집 i에 대해 필요한 최소 시간은 T[i] + X[i] 입니다.
3. 전체 필요한 시간은 이 두 값들 중 최대값입니다.
"""


def solution2():
    # 입력 받기: 집의 개수 N, 각 집의 위치 X, 각 집의 준비 시간 T
    N = int(input().strip())
    X = list(map(int, input().split()))
    T = list(map(int, input().split()))

    # [1] 기본 왕복 시간: 오른쪽 끝 집까지 갔다가 0으로 돌아오는 시간
    base_time = 2 * X[-1]

    # [2] 각 집에 대해 T[i] + X[i] (해당 집에서 물건을 회수하기 위한 최소 시간) 계산
    required_time = base_time
    for i in range(N):
        # 각 집의 최소 요구 시간이 base_time보다 클 수 있으므로, 둘 중 큰 값을 선택
        required_time = max(required_time, T[i] + X[i])

    # [3] 최종 필요한 시간을 출력합니다.
    print(required_time)


if __name__ == '__main__':
    solution2()
