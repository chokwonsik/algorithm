# 반품회수(31964)_3.py
"""
솔루션 3: 수학적 재구성을 통한 계산

문제 설명:
-------------
- N개의 집이 직선 상에 있으며, 각 집 i는 위치 X[i]에 있고, 각 집에서는 T[i] 시간에 물건이 회수됩니다.
- 트럭은 0번 위치(회사)에서 출발하여 모든 집을 방문한 후 다시 0으로 돌아와야 하며, 이동 속도는 1입니다.
- 트럭이 집 i에 도착할 때, 만약 도착 시간이 T[i]보다 빠르면 기다려야 하므로 총 소요 시간이 늘어납니다.

풀이 아이디어:
-------------
- 기본적으로 트럭이 오른쪽 끝 집까지 갔다가 돌아오는 데 걸리는 시간은 2 * X[N-1]입니다.
- 집 i에서 물건을 회수할 수 있으려면, 집 i에 도착하는 시간이 최소 T[i]여야 합니다.
- 집 i에 대해 “필요한 최소 시간”은 사실상 T[i] + X[i] 입니다.
- 이를 2 * X[N-1]와 비교하면, 추가로 기다려야 하는 시간이 있다면 그 차이가 T[i] + X[i] - 2 * X[N-1]으로 나타납니다.
- 음수가 나오면 기다릴 필요가 없는 것이므로, 각 집에 대해 계산한 값이 음수인 경우는 0으로 처리합니다.
- 이를 수식으로 표현하면 최종 답은:
  **2 * X[N-1] + max(0, max_{i}(T[i] + X[i] - 2 * X[N-1]))**
- 코드에서는 T[i] - (2 * X[N-1] - X[i])를 계산하는데, 이는 T[i] + X[i] - 2 * X[N-1]과 동일합니다.

알고리즘 상세:
-------------
1. 각 집 i에 대해 p[i] = T[i] - (2 * X[N-1] - X[i])를 계산합니다.
2. p[i]는 T[i] + X[i] - 2 * X[N-1]와 같으므로, p[i]가 양수인 경우 추가 대기 시간이 필요합니다.
3. 모든 p[i] 값 중 최대값을 구하고, 음수면 0으로 간주합니다.
4. 최종 답은 2 * X[N-1] + (최대 추가 대기 시간) 입니다.
"""


def solution3():
    # 입력 받기: 집의 개수 n, 각 집의 위치 x, 각 집의 준비 시간 t
    n = int(input())
    x = list(map(int, input().split()))
    t = list(map(int, input().split()))

    # [1] 각 집에 대해 추가로 기다려야 할 시간을 계산합니다.
    # 계산식: p[i] = t[i] - (2 * x[-1] - x[i])
    # 이 식은 t[i] + x[i] - 2 * x[-1]와 같으므로, 집 i에서 필요한 최소 시간과 기본 왕복 시간의 차이를 나타냅니다.
    p = [t[i] - (2 * x[-1] - x[i]) for i in range(n)]

    # [2] 모든 집에 대해 계산한 추가 대기 시간 중 최대값을 구합니다.
    # 만약 모든 값이 음수라면, 추가 대기 시간이 없으므로 max_wait = 0
    max_wait = max(p)

    # [3] 최종 필요한 시간은 기본 왕복 시간(2 * x[-1])에, 추가 대기 시간이 필요한 경우 그 값을 더한 것입니다.
    # max_wait가 음수이면 0을 더합니다.
    print(2 * x[-1] + max(0, max_wait))


if __name__ == '__main__':
    solution3()
