# 반품회수(31964)_1.py
"""
솔루션 1: 역순 접근 방식

문제 설명:
-------------
- N개의 집이 직선 상에 있고, 각 집 i는 위치 X[i]에 있습니다.
- 각 집 i에서는 시각 T[i]에 물건이 회수됩니다.
- 트럭은 0번 위치(회사)에서 시각 0에 출발하여, 모든 집의 물건을 회수한 뒤 다시 0으로 돌아와야 합니다.
- 트럭의 이동 속도는 1이며, 거리에 비례하여 시간이 소요됩니다.
- 집의 물건은 트럭이 해당 집의 위치를 시각 T[i] 이상에 지나면 바로 회수할 수 있으므로, 물건 회수에는 시간이 들지 않습니다.

풀이 아이디어:
-------------
- 트럭은 오른쪽에 있는 모든 집을 방문한 후 다시 회사로 돌아와야 하므로, 기본 왕복 시간은 2 * X[N-1] 입니다.
- 그러나, 각 집에서는 물건이 준비되는 T[i] 시간에 도달해야 하므로, 도착 시간이 너무 빠르면 기다려야 합니다.
- 솔루션 1은 오른쪽 끝 집(가장 큰 X값)부터 시작하여 왼쪽으로 이동하면서, 각 집 사이의 이동 시간과 필요한 대기 시간을 누적하여 계산합니다.
- 마지막으로, 첫 번째 집에서 회사(0)로 돌아가는 이동 시간(X[0])을 더해 최종 시간을 구합니다.

알고리즘 상세:
-------------
1. 마지막 집에서는 트럭이 도착할 수 있는 최소 시간은 집의 위치(X[N-1])와 그 집의 준비 시간(T[N-1]) 중 큰 값입니다.
2. 오른쪽 끝 집부터 차례로 왼쪽으로 이동하면서:
   - 현재 집과 바로 오른쪽 집 사이의 이동 거리를 더합니다.
   - 만약 이동 후의 누적 시간이 해당 집의 준비 시간보다 작다면, 준비 시간까지 기다립니다.
3. 모든 집을 처리한 후, 첫 번째 집에서 0까지의 이동 거리를 더합니다.
4. 최종 누적된 시간이 모든 물건을 회수하고 돌아오는 데 필요한 최소 시간입니다.
"""


def solution1():
    # 입력 받기: 집의 개수 N, 각 집의 위치 X, 각 집의 준비 시간 T
    N = int(input().strip())
    X = list(map(int, input().split()))
    T = list(map(int, input().split()))

    # [1] 마지막 집(오른쪽 끝 집)에서의 시작 시간 결정
    # 집에 도착하는 최소 시간은 집의 위치와 해당 집의 준비 시간 중 큰 값이어야 합니다.
    time = max(X[-1], T[-1])

    # [2] 오른쪽 끝 집부터 왼쪽 방향으로 이동하면서 시간 누적하기
    # i는 인덱스로 N-2부터 0까지 (즉, 오른쪽 끝 바로 왼쪽 집부터 첫 번째 집까지)
    for i in range(N - 2, -1, -1):
        # 현재 집(X[i])와 오른쪽 집(X[i+1]) 사이의 이동 거리를 계산합니다.
        travel = X[i + 1] - X[i]
        # 이동 시간을 누적합니다.
        time += travel

        # 만약 현재 집에 도착한 시간이 아직 준비 시간 T[i]보다 작다면,
        # T[i]까지 기다려야 하므로, 시간을 T[i]로 업데이트합니다.
        if time < T[i]:
            time = T[i]

    # [3] 모든 집을 처리한 후, 첫 번째 집에서 회사(위치 0)까지 이동하는 시간을 더합니다.
    time += X[0]

    # [4] 최종 계산된 시간을 출력합니다.
    print(time)


if __name__ == '__main__':
    solution1()
