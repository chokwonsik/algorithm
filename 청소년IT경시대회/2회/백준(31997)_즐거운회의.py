# =============================================================================
# 문제: 즐거운 회의
# -----------------------------------------------------------------------------
# 이 문제에서는 N명의 사람들이 회의에 참여해요.
# 각 사람은 정해진 시간에 회의에 들어오고, 정해진 시간에 회의에서 나가요.
#
# 또한, 몇몇 사람들은 서로 친구예요.
# 회의에 있는 동안, 친구인 두 사람이 같이 있으면 "즐거운 대화"를 할 수 있어요.
#
# 우리 목표:
#  - 회의 시간 동안(0.5, 1.5, ..., T-0.5)마다 같이 있는 친구 쌍의 수를 구하는 거예요.
#
# 입력:
#  1. 첫 번째 줄: 사람의 수 N, 친구 관계 수 M, 회의 종료 시간 T
#  2. 다음 N개의 줄: 각 사람의 회의에 들어오는 시간(a)과 나가는 시간(b)
#     (a는 회의에 들어오는 시간, b는 회의에서 나가는 시간이며 a < b예요)
#  3. 그 다음 M개의 줄: 서로 친구인 두 사람의 번호 (예: 1 2)
#
# 출력:
#  - 각 시간 t = 0.5, 1.5, ..., T-0.5마다 회의에 참석한 친구 쌍의 수를 한 줄씩 출력해요.
#
# 풀이 아이디어:
#  1. 각 시간마다 회의에 들어오고 나가는 사람을 미리 저장해요.
#     - in_meeting[시간]에는 그 시간에 들어오는 사람들을 저장해요.
#     - out_meeting[시간]에는 그 시간에 나가는 사람들을 저장해요.
#
#  2. 각 사람의 친구 목록을 미리 인접 리스트 형태로 저장해요.
#
#  3. 회의 시간 동안, 현재 회의에 참석 중인 사람을 joined 리스트로 관리해요.
#
#  4. 시간이 진행될 때마다:
#     - 회의에서 나가는 사람들은 joined 리스트에서 False로 바꾸고,
#       그 사람과 같이 있던 친구가 있다면 친구 쌍 수를 줄여요.
#
#     - 회의에 들어오는 사람들은 joined 리스트에서 True로 바꾸고,
#       이미 회의에 있던 친구가 있다면 친구 쌍 수를 늘려요.
#
#  5. 각 시간마다 계산된 친구 쌍의 수를 출력해요.
#
# =============================================================================

# 입력받은 첫 번째 줄에서 사람의 수(n), 친구 관계 수(m), 회의 종료 시간(t)를 읽어요.
n, m, t = map(int, input().split())

# 각 시간별로 회의에 들어오는 사람들의 번호를 저장할 리스트를 만들어요.
# 예를 들어, in_meeting[3]은 시간 3에 회의에 들어오는 사람들의 번호를 저장해요.
in_meeting = [[] for _ in range(t + 1)]

# 각 시간별로 회의에서 나가는 사람들의 번호를 저장할 리스트를 만들어요.
# 예를 들어, out_meeting[4]는 시간 4에 회의에서 나가는 사람들의 번호를 저장해요.
out_meeting = [[] for _ in range(t + 1)]

# 각 사람의 친구 관계를 저장할 인접 리스트를 만들어요.
# friends[i]에는 i번 사람과 친구인 사람들의 번호가 들어 있어요.
friends = [[] for _ in range(n + 1)]

# 다음 N개의 줄에서 각 사람의 회의 시작 시간(a)과 종료 시간(b)를 읽어요.
# 각 사람의 번호는 1부터 시작해요.
for i in range(1, n + 1):
    a, b = map(int, input().split())
    in_meeting[a].append(i)   # a 시간에 i번 사람이 회의에 들어와요.
    out_meeting[b].append(i)  # b 시간에 i번 사람이 회의에서 나가요.

# 다음 M개의 줄에서 두 사람이 서로 친구임을 나타내는 정보를 읽어요.
# 친구 관계는 양방향이에요. 즉, u와 v가 친구면, u의 친구 목록에 v, v의 친구 목록에 u를 추가해요.
for _ in range(m):
    u, v = map(int, input().split())
    friends[u].append(v)
    friends[v].append(u)

# joined 리스트는 각 사람이 현재 회의에 참석 중인지(True/False)를 나타내요.
# 처음에는 아무도 회의에 없으므로 모두 False로 설정해요.
joined = [False] * (n + 1)

# total_relations는 현재 회의에 참석 중인 친구 쌍의 총 수를 저장해요.
total_relations = 0

# 회의 시간은 0부터 t-1까지 진행돼요.
# 실제 출력은 t=0.5, 1.5, ..., T-0.5 시간에 출력되지만,
# 회의에 들어오고 나가는 이벤트는 정수 시간에 발생해요.
for i in range(t):
    # 먼저, i 시간에 회의에서 나가는 사람들을 처리해요.
    for u in out_meeting[i]:
        joined[u] = False  # u번 사람이 회의에서 나가요.
        # u번 사람의 친구 목록을 확인하면서,
        # 회의에 아직 남아있는 친구가 있으면 친구 쌍의 수를 1 줄여요.
        for v in friends[u]:
            if joined[v]:
                total_relations -= 1

    # 그 다음, i 시간에 회의에 들어오는 사람들을 처리해요.
    for u in in_meeting[i]:
        joined[u] = True  # u번 사람이 회의에 들어와요.
        # u번 사람의 친구 목록을 확인하면서,
        # 이미 회의에 참석 중인 친구가 있으면 친구 쌍의 수를 1 늘려요.
        for v in friends[u]:
            if joined[v]:
                total_relations += 1

    # i 시간 후, 실제 시간은 i + 0.5 (예: 0.5, 1.5, ...) 이며,
    # 이 시간에 회의에 함께 있는 친구 쌍의 수를 출력해요.
    print(total_relations)
